#include "libft.h"

void	ft_putmap_fd(char **s, int fd)
{
	if (!s)
		return ;
	while (*s)
	{
		ft_putstr_fd(*s, fd);
		ft_putchar_fd('\n', fd);
		s++;
	}
}
